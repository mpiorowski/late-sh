fn big_text<'a, S: AsRef<str>>(text: Vec<S>, color_1: Color, color_2: Color) -> Paragraph<'a> {
    let lines = text
        .iter()
        .map(|line| {
            let spans = line
                .as_ref()
                .chars()
                .map(|c| {
                    if c == '█' {
                        Span::styled("█", Style::default().fg(color_1))
                    } else {
                        Span::styled(c.to_string(), Style::default().fg(color_2))
                    }
                })
                .collect::<Vec<Span>>();
            Line::from(spans)
        })
        .collect::<Vec<Line>>();
    Paragraph::new(lines).centered()
}

