pub fn dash(color: Color) -> Paragraph<'static> {
    let rows = [
        "            ",
        "            ",
        "  ████████  ",
        "  ████████  ",
        "            ",
        "            ",
    ];
    let lines = rows
        .iter()
        .map(|s| Line::from(Span::styled(*s, Style::default().fg(color))))
        .collect::<Vec<_>>();
    Paragraph::new(lines)
}

pub fn palette_colors(palette: sshattrick_core::Palette) -> (Color, Color) {
    match palette {
        sshattrick_core::Palette::Dark => (Color::Cyan, Color::White),
        sshattrick_core::Palette::Light => (Color::DarkGray, Color::Gray),
        sshattrick_core::Palette::Basket => (Color::Magenta, Color::LightMagenta),
        sshattrick_core::Palette::Alt => (Color::Green, Color::Red),
    }
}
